import pandas as pd
import numpy as np
from sklearn.model_selection import train_test_split
from sklearn.ensemble import RandomForestRegressor
from sklearn.preprocessing import LabelEncoder
import pickle

# 1. Load the dataset
df = pd.read_csv('Salary_Dataset.csv')

# 2. Select useful features and drop missing values
# We will use Rating, Location, and Job Roles to predict Salary
df = df[['Rating', 'Location', 'Job Roles', 'Salary']]
df = df.dropna()

# 3. Encode categorical variables into numbers
le_location = LabelEncoder()
df['Location'] = le_location.fit_transform(df['Location'])

le_job_role = LabelEncoder()
df['Job Roles'] = le_job_role.fit_transform(df['Job Roles'])

# 4. Split data into Features (X) and Target (y)
X = df.drop('Salary', axis=1)
y = df['Salary']

# 5. Train a Regression Model
regressor = RandomForestRegressor(random_state=42)
regressor.fit(X, y)

# 6. Save the model and encoders for the Streamlit app
data = {
    "model": regressor,
    "le_location": le_location,
    "le_job_role": le_job_role
}

with open('saved_steps.pkl', 'wb') as file:
    pickle.dump(data, file)

import streamlit as st
import pickle
import numpy as np

# 1. Load the saved model and encoders
def load_model():
    with open('saved_steps.pkl', 'rb') as file:
        data = pickle.load(file)
    return data

data = load_model()
regressor = data["model"]
le_location = data["le_location"]
le_job_role = data["le_job_role"]

# 2. Build the Streamlit User Interface
st.title("Tech Salary Prediction App")
st.write("Predict your estimated salary based on Location, Job Role, and Company Rating!")

# 3. Create input fields for the user
locations = le_location.classes_
job_roles = le_job_role.classes_

location = st.selectbox("Select Location", locations)
job_role = st.selectbox("Select Job Role", job_roles)
rating = st.slider("Company Rating", 1.0, 5.0, 3.5, 0.1)

# 4. Prediction Logic
# Convert user text input back to encoded numbers
loc_encoded = le_location.transform([location])
role_encoded = le_job_role.transform([job_role])

# Combine all features into a single input array for prediction
X_input = np.array([rating, loc_encoded[0], role_encoded[0]]).reshape(1, -1)

# The if st.button is for the Streamlit app context, not for sequential execution here.
# The prediction logic will now run when the cell is executed.

# Predict
salary_pred = regressor.predict(X_input)

st.subheader(f"The estimated salary is: ₹ {salary_pred[0]:,.2f}")
